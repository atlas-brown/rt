def traverse_node(nd : AstNode, pipelines_nodes : list[AstNode]):
    match nd:
        case CommandNode():
            return
        case DefunNode():
            return
        case IfNode():
            traverse_node(nd.cond, pipelines_nodes)
            traverse_node(nd.then_b, pipelines_nodes)
            traverse_node(nd.else_b, pipelines_nodes)
        case AndNode():
            traverse_node(nd.left_operand, pipelines_nodes)
            traverse_node(nd.right_operand, pipelines_nodes)
        case OrNode():
            traverse_node(nd.left_operand, pipelines_nodes)
            traverse_node(nd.right_operand, pipelines_nodes)
        case NotNode():
            traverse_node(nd.body, pipelines_nodes)
        case ForNode():
            traverse_node(nd.body, pipelines_nodes)
        case WhileNode():
            traverse_node(nd.test, pipelines_nodes)
            traverse_node(nd.body, pipelines_nodes)
        case CaseNode():
            for case in nd.cases:
                traverse_node(case["cbody"], pipelines_nodes)
        case PipeNode():
            pipelines_nodes.append(nd)
        case SemiNode():
            traverse_node(nd.left_operand, pipelines_nodes)
            traverse_node(nd.right_operand, pipelines_nodes)
        case SubshellNode():
            traverse_node(nd.body, pipelines_nodes)
        case RedirectionNode():
            return
        case _:
            logging.debug(f"Node not handled: {nd}")
    

def traverse_raw_nodes(ls : list[RawNode]) -> list[AstNode]:
    pipeline_nodes = []
    for nd in ls:
        traverse_node(nd.ast_node, pipeline_nodes)
    return pipeline_nodes

def combine_maps(ls_m : list[NodeMap]) -> NodeMap:
    """
    Combine the dictionaries generated by each symbolic path such that:
    raw_text pick any (shouldn't matter)
    conditional Take or
    visited Take Or of all
    constant Take And of all
    """
    if not ls_m:
        return {}
    result = {}
    all_keys = set().union(*ls_m)
    
    for key in all_keys:
        nodes = [m[key] for m in ls_m if key in m]
        
        # Pick any raw_text (shouldn't matter)
        raw_text = nodes[0].raw_text

        # Assert that all conditionals are the same, then pick any
        cmb_cond = ConditionalVisit()
        for cnd in [node.conditional for node in nodes]:
            if cmb_cond is None or cnd is None:
                cmb_cond = cnd 
            else:
                cmb_cond.combine(cnd)
        conditional = cmb_cond

        # Take Or of all visited
        visited = any(node.visited for node in nodes)

        # Take And of all constant
        constant = all(node.constant for node in nodes)

        result[key] = ASTNodeVisit(
            raw_text=raw_text,
            conditional=conditional,
            visited=visited,
            constant=constant
        )

    return result
    