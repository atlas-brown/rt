@dataclass(frozen=True)
class RawNode:
    ast_node: AstNode
    rawtext: str
    line_before: int
    line_after: int  # relevant for mysterious shell reasons


@dataclass()
class ConditionalVisit:
    true_followed : bool = False
    false_followed : bool = False

    def combine(self,other:ConditionalVisit):
        self.true_followed = self.true_followed or other.true_followed
        self.false_followed = self.false_followed or other.false_followed
@dataclass()
class ASTNodeVisit:
    raw_text : str
    conditional : Optional[ConditionalVisit] = None  #if cond ,while cond, first arg of and/or , not arg
    visited : bool = False 
    constant : bool = True
    
    def set_visited(self):
        self.visited = True
    
    def set_not_constant(self):
        self.constant = False
        
NodeMap = dict[int,ASTNodeVisit]


